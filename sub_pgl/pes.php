<p>Ao se formar nesse curso, você irá a recepção, sendo responsável por alistamentos. <br>
Para assumir um superior, é preciso estar a UM quadrado de distância da cadeira do mesmo e digitar:
"Assumo, senhor." <br>
Por exemplo: Assumo, senhora Suprema Sara68671.</p>

<br>

<p>Há 4 requisitos primordiais para realizar o alistamento, são eles: <br>
Fardamento, missão, grupo e balão de fala.</p>

<br>

<p>- O fardamento é o mesmo que você está utilizando no momento, camisa e calça com bolsos e
sapatos redondos, todos de cor PRETA, sem nenhum tipo de acessócios.<br>
- A missão do civil deve estar em branco, sem nada escrito; <br>
- O grupo favoritado deverá ser o aberto do SSH, cujo o dono é o usuário Victor09305; <br>
- O balão de fala tem de ser de COR BRANCA;</p> <br>

<p>No final do alistamento, peça para o civil se virar e verifique se não há números nas costas. <br>
